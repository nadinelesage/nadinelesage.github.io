  <h5>Le massage assis par excellence</h5>
  <p>
    Le Amma assis est une solution efficace, pratique et simple à intégrer en
    entreprise dans une démarche de prévention santé. Cette technique permet
    de  lutter contre le stress (provoquées par une surcharge de travail, une
    atmosphère compétitive,…), de soulager les douleurs posturales et de prévenir
    les troubles musculo-squelettiques.
  </p>
  <div id="amas-assis-more" class="d-none">
    <h5>Une technique ancestrale japonaise</h5>
    <p>
      Le Amma assis est un enchainement précis et technique composé de pressions,
      stimulation de séries de points d’accupressions, étirements, balayages et
      percussions, sur l’ensemble du haut du corps qui soulage efficacement les
      tensions et apporte détente physique et mentale.<br />
      Ce massage se pratique habillé, sur chaise ergonomique créée spécialement
      pour cette technique.<br />
      <span class="font-weight-bold">
        La particularité de cette technique est de procurer à la fois une profonde
        détente corporelle et mentale ainsi qu’une dynamisation de la personne en
        un minimum de temps (la durée de la séance est en effet modulable), ne
        nécessite que très peu de place et se reçoit sur un temps de pause. Aucun
        matériel à fournir.
      </strong>
    </p>
